private void ListarCategoria() { 
try {

            CategoriaDAO objCategoriaDAO = new CategoriaDAO();
            DefaultTableModel model = (DefaultTableModel) TBCategoria.getModel();
            model.setNumRows(0);
            ArrayList<CategoriaDTO> lista
                    = objCategoriaDAO.PesquisarCategoria();
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNomecategoria(),
                    
                  
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "FrmPesquisar ListarCategoria\n" + e);
        }

}
 private void CarregarCamposCategoria(){
      int setar = TBCategoria.getSelectedRow();
      frmcategoria.txtid.setText(TBCategoria.getModel().getValueAt(setar, 0).toString());   
         frmcategoria.txtnomecategoria.setText(TBCategoria.getModel().getValueAt(setar, 1).toString());
      
      
     }
